# Take a number from the user and check if it is positive, negative, or zero.
num = float(input("Enter a number:")) # Input a number from the user
if num > 0: # Check if the number is positive
    print("The number is positive.") # Print if the number is positive
elif num < 0: # Check if the number is negative
    print("The number is negative.") # Print if the number is negative
else: # If the number is neither positive nor negative
    print("The number is zero.") # Print if the number is zero
# End of the program